/**
 * Остановка автобуса. Имеет порядковый номер.
 */
public class BusStop {
    private int busStopStation;


    public void setBusStopStation(int busStopStation) {
        this.busStopStation = busStopStation;
    }

    public int getBusStopStation() {
        return busStopStation;
    }

}
